void wiretest(){
  int gpio = 36;
  int twait = 1000;

  Serial.printf(" GPIO OUT: %i: ", gpio);
  for (int i=0; i<10000; i++){
    pinMode(gpio,   OUTPUT);
    digitalWrite(gpio, LOW);
    mydelay2(twait,20);
    digitalWrite(gpio, HIGH);
    mydelay2(twait,20);
    Serial.printf(".");
  }
  Serial.printf("\n");

  Serial.printf(" GPIO IN: %i: ", gpio);
  pinMode(gpio,  INPUT);
  for (int i=0; i<20; i++){
    int dio = digitalRead(gpio);
    mydelay2(twait/2,20);
    if(dio)
      Serial.printf("1");
    else
      Serial.printf("0");
  }
  Serial.printf("   -> Done! \n");
  while(1);
}


